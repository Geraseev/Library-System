    private void tabelaLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaLMouseClicked
        txtCod.setText(tabelaL.getValueAt(tabelaL.getSelectedRow(), 0).toString());
        txtTit.setText(tabelaL.getValueAt(tabelaL.getSelectedRow(), 1).toString());
        txtAut.setText(tabelaL.getValueAt(tabelaL.getSelectedRow(), 2).toString());
        txtEd.setText(tabelaL.getValueAt(tabelaL.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tabelaLMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            Livros obj = new Livros();
            
            obj.setIdlivro(Integer.parseInt(txtCod.getText()));
            obj.setTitulo(txtTit.getText());
            obj.setAutor(txtAut.getText());
            obj.setEditora(txtEd.getText());

            LivrosDAO dao = new LivrosDAO();
            dao.editarLiv(obj);

            JOptionPane.showMessageDialog(null, "Editado com sucesso");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "falha" +e); 
        }
        limpar();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            Livros obj = new Livros();
            
            obj.setIdlivro(Integer.parseInt(txtCod.getText()));

            LivrosDAO dao = new LivrosDAO();
            dao.excluirLiv(obj);

            JOptionPane.showMessageDialog(null, "Excluido com sucesso");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "falha" +e); 
        }
        limpar();
    }//GEN-LAST:event_jButton3ActionPerformed

