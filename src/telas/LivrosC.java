    public void Listar(){
        try {
            LivrosDAO dao = new LivrosDAO();
            List<Livros> listaU = dao.listarLivros();
            DefaultTableModel modelo = (DefaultTableModel)tabelaL.getModel();
            modelo.setNumRows(0);
            
            for(Livros v : listaU){
                modelo.addRow(new Object[]{
                v.getIdlivro(),
                v.getTitulo(),
                v.getAutor(),
                v.getEditora()
                });
            }
        } catch (Exception e) {
        }
    }
    
