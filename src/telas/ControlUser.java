    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            User obj = new User();
            
            obj.setIduser(Integer.parseInt(txtCod.getText()));

            UsuarioDAO dao = new UsuarioDAO();
            dao.excluirUser(obj);

            JOptionPane.showMessageDialog(null, "Excluido com sucesso");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "falha" + e); 
        }
        limpar();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String nome = txtCon.getText();
            
            UsuarioDAO dao = new UsuarioDAO();
            List<User> listaU = dao.listarUserNome(nome);
            DefaultTableModel modelo = (DefaultTableModel)tabelaU.getModel();
            modelo.setNumRows(0);
            
            for(User v : listaU){
                modelo.addRow(new Object[]{
                v.getIduser(),
                v.getNome(),
                v.getCpf(),
                v.getTel()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Usuário não encontrado"); 
        }
    }//GEN-LAST:event_jButton4ActionPerformed

