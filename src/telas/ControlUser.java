    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            User obj = new User();
            
            obj.setNome(txtNomeUs.getText());
            obj.setCpf(txtCpf.getText());
            obj.setTel(txtTel.getText());

            UsuarioDAO dao = new UsuarioDAO();
            dao.cadastraUser(obj);

            JOptionPane.showMessageDialog(null, "cadastro realizado com sucesso");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "falha" + e); 
        }
        limpar();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tabelaUMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaUMouseClicked
        txtCod.setText(tabelaU.getValueAt(tabelaU.getSelectedRow(), 0).toString());
        txtNomeUs.setText(tabelaU.getValueAt(tabelaU.getSelectedRow(), 1).toString());
        txtCpf.setText(tabelaU.getValueAt(tabelaU.getSelectedRow(), 2).toString());
        txtTel.setText(tabelaU.getValueAt(tabelaU.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tabelaUMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            User obj = new User();
            
            obj.setIduser(Integer.parseInt(txtCod.getText()));
            obj.setNome(txtNomeUs.getText());
            obj.setCpf(txtCpf.getText());
            obj.setTel(txtTel.getText());

            UsuarioDAO dao = new UsuarioDAO();
            dao.editarUser(obj);

            JOptionPane.showMessageDialog(null, "Edição realizada com sucesso");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "falha" + e); 
        }
        limpar();
    }//GEN-LAST:event_jButton2ActionPerformed

