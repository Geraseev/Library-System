    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                 
        String cmdsql = "insert into reserva(idliv,titulo,dataE,dataR) values(?,?,?,?)";
        
        try {
            
            stmt = conecta.prepareStatement(cmdsql);
            
            stmt.setInt(1, Integer.parseInt(txtCodL.getText()));
            stmt.setString(2, txtTit.getText());
            stmt.setString(3, ((JTextField)txtDataE.getDateEditor().getUiComponent()).getText());
            stmt.setString(4, ((JTextField)txtDataR.getDateEditor().getUiComponent()).getText());
            
            stmt.execute();
            
            JOptionPane.showMessageDialog(null, "reservado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro na reserva " +e);
        }
        ListarR();
        
               
    }//GEN-LAST:event_jButton2ActionPerformed

